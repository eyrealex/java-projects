public class Cat {
    String name;
    String favFood;
    int age;

    public Cat(String name) {
        this.name = name;
        this.favFood = favFood;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public String getFavFood() {
        return favFood;
    }

    public void setFavFood(String favFood) {
        this.favFood = favFood;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
